			<h2>Introduction</h2>
			<figure>
				<img src="images/herald.jpg" alt="A man in heraldic clothing holds a pointer, as if teaching.">
				<figcaption>Thomas Hawley, Clarenceux King of Arms</figcaption>
			</figure>
			<p><strong>Heraldry</strong> (/ˈhɛrəldri/) is a broad term, encompassing the design, display, and study of armorial bearings (known as <strong>armory</strong>), as well as related disciplines, such as vexillology, together with the study of ceremony, rank, and pedigree. Armory, the best-known branch of heraldry, concerns the design and transmission of the heraldic achievement. The achievement, or armorial bearings usually includes a coat of arms on a shield, helmet, and crest, together with any accompanying devices, such as supporters, badges, heraldic banners, and mottoes.</p>
			<p>Although the use of various devices to signify individuals and groups goes back to antiquity, both the form and use of such devices varied widely, and the concept of regular, hereditary designs, constituting the distinguishing feature of heraldry, did not develop until the High Middle Ages. It is very often claimed that the use of helmets with face guards during this period made it difficult to recognize one's commanders in the field when large armies gathered together for extended periods, necessitating the development of heraldry as a symbolic language, but there is very little actual support for this view.</p>
			<p>The beauty and pageantry of heraldic designs allowed them to survive the gradual abandonment of armour on the battlefield during the seventeenth century. Heraldry has been described poetically as "the handmaid of history", "the shorthand of history", and "the floral border in the garden of history". In modern times, individuals, public and private organizations, corporations, cities, towns, and regions use heraldry and its conventions to symbolize their heritage, achievements, and aspirations.</p>
			
			<h2>Elements of a Heraldic Achievement</h2>
			<figure>
				<img src="images/shield.png" alt="A blank coat of arms, with all elements of an achievement present.">
				<figcaption>Diagram of a heraldic achievement</figcaption>
			</figure>
			<p>A heraldic <strong>achievement</strong> consists of a shield of arms the <strong>coat of arms</strong>, or simply coat, together with all of its accompanying elements, such as a crest, supporters, and other heraldic embellishments. The term "coat of arms" technically refers to the shield of arms itself, but the phrase is commonly used to refer to the entire achievement. The one indispensable element of a coat of arms is the shield; many ancient coats of arms consist of nothing else, but no achievement or armorial bearings exists without a coat of arms.</p>
			<p>From a very early date, illustrations of arms were frequently embellished with helmets placed above the shields. These in turn came to be decorated with fan-shaped or sculptural crests, often incorporating elements from the shield of arms; as well as a wreath or torse, or sometimes a coronet, from which depended the lambrequin or mantling. To these elements, modern heraldry often adds a <strong>motto</strong> displayed on a ribbon, typically below the shield.</p>
			<p>If the bearer is entitled to the ribbon, collar, or badge of a knightly order, it may encircle or depend from the shield. Some arms, particularly those of the nobility, are further embellished with <strong>supporters</strong>, heraldic figures standing alongside or behind the shield; often these stand on a <strong>compartment</strong>, typically a mound of earth and grass, on which other badges, symbols, or heraldic banners may be displayed.</p>
			
			<h2>Shield (Escutcheon)</h2>
			<p>The primary element of an heraldic achievement is the shield, or <strong>escutcheon</strong>, upon which the coat of arms is depicted. All of the other elements of an achievement are designed to decorate and complement these arms, but only the shield of arms is required. The shape of the shield, like many other details, is normally left to the discretion of the heraldic artist, and many different shapes have prevailed during different periods of heraldic design, and in different parts of Europe.</p>
			<p>One shape alone is normally reserved for a specific purpose: the <strong>lozenge</strong>, a diamond-shaped escutcheon, was traditionally used to display the arms of women, on the grounds that shields, as implements of war, were inappropriate for this purpose. This distinction was not always strictly adhered to, and a general exception was usually made for sovereigns, whose arms represented an entire nation. Sometimes an oval shield, or <strong>cartouche</strong>, was substituted for the lozenge; this shape was also widely used for the arms of clerics in French, Spanish, and Italian heraldry, although it was never reserved for their use.</p>
			<p>The whole surface of the escutcheon is termed the <strong>field</strong>, which may be plain, consisting of a single tincture, or divided into multiple sections of differing tinctures by various lines of partition; and any part of the field may be semé, or powdered with small charges. The edges and adjacent parts of the escutcheon are used to identify the placement of various heraldic charges; the upper edge, and the corresponding upper third of the shield, are referred to as the <strong>chief</strong>; the lower part is the <strong>base</strong>. The sides of the shield are known as the <strong>dexter</strong> and <strong>sinister</strong> flanks, although it is important to note that these terms are based on the point of view of the bearer of the shield, who would be standing behind it; accordingly the side which is to the bearer's right is the dexter, and the side to the bearer's left is the sinister, although to the observer, and in all heraldic illustration, the dexter is on the left side, and the sinister on the right.</p>
			
